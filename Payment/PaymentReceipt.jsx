const formatMoney = (value) =>
  `Rs. ${Number(value || 0).toLocaleString("en-IN")}`;

const formatReceiptDate = (value) => {
  if (!value) return "-";
  const date = new Date(value);
  return Number.isNaN(date.getTime())
    ? "-"
    : date.toLocaleString("en-IN", {
        dateStyle: "medium",
        timeStyle: "short",
      });
};

const readValue = (value) =>
  value === undefined || value === null || value === "" ? "-" : value;

const normalizeReceiptData = (receipt = {}) => ({
  paymentId: readValue(receipt.paymentId || receipt.transactionId || receipt._id),
  paidAt: formatReceiptDate(receipt.paidAt),
  amount: formatMoney(receipt.amount),
  studentName: readValue(receipt.studentName || receipt.name),
  fatherName: readValue(receipt.fathername),
  course: readValue(receipt.course),
  semester: readValue(receipt.semester),
  enrollment: readValue(receipt.enrollment),
  paymentMethod: readValue(receipt.paymentMethod),
});

