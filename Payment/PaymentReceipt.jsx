export const printPaymentReceipt = (receipt, receiptWindow) => {
  const printWindow =
    receiptWindow || window.open("", "_blank", "width=1400,height=900");

  if (!printWindow) {
    window.alert("Popup blocked. Please allow popups to print the receipt.");
    return;
  }

  printWindow.document.open();
  printWindow.document.write(getReceiptDocumentHtml(receipt));
  printWindow.document.close();
  printWindow.focus();

  window.setTimeout(() => {
    printWindow.print();
    printWindow.close();
  }, 500);
};

